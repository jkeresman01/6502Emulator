#pragma once

namespace emulator6502
{

//////////////////////////////////////////////////////////////
///
/// @class AbstractComponent
///
/// @brief Base interface for all emulator UI/hardware components
///        (e.g., displays, panels, LEDs).
///
/// Provides a consistent lifecycle for initialization,
/// rendering, and cleanup.
///
//////////////////////////////////////////////////////////////
class AbstractComponent
{
public:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Virtual destructor to ensure proper cleanup
    ///        of derived classes.
    ///
    //////////////////////////////////////////////////////////////
    virtual ~AbstractComponent() = default;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Initializes the component.
    ///        Called before rendering begins.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Init() = 0;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Renders the component within the emulator UI.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Render() = 0;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Cleans up resources used by the component.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Destroy() = 0;
};

} // namespace emulator6502

