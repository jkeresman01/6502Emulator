#pragma once

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////

#include <cstdint>

#include <emulator/components/AbstractComponent.h>

namespace emulator6502
{

//////////////////////////////////////////////////////////////
///
/// @class SwitchPanel
///
/// @brief A UI component that simulates a panel of toggle
///        switches within the emulator.  
///
/// Commonly used to provide binary input to the emulated
/// hardware (e.g., setting values, flags, or control signals).
///
//////////////////////////////////////////////////////////////
class SwitchPanel : public AbstractComponent
{
public:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Initializes the switch panel component.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Init() override;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Renders the switch panel in the emulator UI,
    ///        allowing user interaction with virtual switches.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Render() override;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Cleans up resources used by the switch panel.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Destroy() override;

private:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Number of switches in the panel.
    ///
    //////////////////////////////////////////////////////////////
    static constexpr int SWITCHES_COUNT = 9;
};

} // namespace emulator6502

