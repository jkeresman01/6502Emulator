#pragma once

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////

#include <emulator/components/AbstractComponent.h>

namespace emulator6502
{

//////////////////////////////////////////////////////////////
///
/// @class LEDs
///
/// @brief A UI component that simulates an LED panel within
///        the emulator, typically used to visualize binary
///        output or CPU state.
///
//////////////////////////////////////////////////////////////
class LEDs : public AbstractComponent
{
public:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Initializes the LED component.
    ///        Sets up any necessary state before rendering.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Init() noexcept override;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Renders the LED display in the emulator UI.
    ///        Uses circles with spacing to simulate LEDs.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Render() noexcept override;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Cleans up any resources used by the LED component.
    ///
    //////////////////////////////////////////////////////////////
    virtual void Destroy() noexcept override;

private:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief The radius of each LED (in UI units).
    ///
    //////////////////////////////////////////////////////////////
    static constexpr float RADIUS = 8.0f;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief The spacing between adjacent LEDs.
    ///
    //////////////////////////////////////////////////////////////
    static constexpr float SPACING = 4.0f;
};

} // namespace emulator6502

