#pragma once

#include "imgui.h"

namespace emulator6502
{
////////////////////////////////////////////////////////////
///
/// @class Pixel
///
/// @brief Represents a single pixel in the emulator
///        with a color value.
//
////////////////////////////////////////////////////////////
class Pixel
{
  public:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief Default constructor that initializes the pixel color to black.
    ///
    /// The default color of the pixel is set to black (RGBA: 0, 0, 0, 1).
    ///
    //////////////////////////////////////////////////////////////
    Pixel() = default;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Renders the pixel on the screen using ImGui.
    ///
    //////////////////////////////////////////////////////////////
    void Render() const;

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Gets the current color of the pixel.
    ///
    /// @return The color of the pixel
    ///
    //////////////////////////////////////////////////////////////
    ImVec4 GetColor() const { return m_Color; }

    //////////////////////////////////////////////////////////////
    ///
    /// @brief Sets the color of the pixel.
    ///
    /// @param [in] color The color to set for the pixel
    ///
    //////////////////////////////////////////////////////////////
    void SetColor(const ImVec4 &color) { m_Color = color; }

  private:
    //////////////////////////////////////////////////////////////
    ///
    /// @brief The color of the pixel, defaulting to black (RGBA: 0, 0, 0, 1).
    ///
    //////////////////////////////////////////////////////////////
    ImVec4 m_Color = ImVec4(0, 0, 0, 1);

    //////////////////////////////////////////////////////////////
    ///
    /// @brief The fixed size of the pixel, set to 6.0f.
    ///
    //////////////////////////////////////////////////////////////
    static constexpr float PIXEL_SIZE = 6.0f;
};

} // namespace emulator6502
